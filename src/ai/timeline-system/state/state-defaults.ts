/**
 * AI-PET-WORLD
 * 时间线系统 - 状态层默认值定义（第一版）
 *
 * ----------------------------------------------------------------------------
 * 【这个文件负责什么？】
 * ----------------------------------------------------------------------------
 * 这个文件负责定义 timeline-system 中“状态层”的默认初始值。
 *
 * 它的核心任务是：
 * 1. 给每种状态提供一份稳定、可运行的默认值
 * 2. 给宠物出生后进入时间线系统时，提供第一份状态快照
 * 3. 给后续 state-updater.ts 提供统一起点
 * 4. 给 timeline-gateway.ts 提供基础初始化方法
 *
 * ----------------------------------------------------------------------------
 * 【这个文件不负责什么？】
 * ----------------------------------------------------------------------------
 * 这个文件不负责：
 * - 根据先天人格定制初始状态
 * - 根据事件更新状态
 * - 根据时间推进状态
 * - 根据时序偏移修正状态
 * - 根据状态计算行为
 *
 * 这里先只做“默认状态骨架”。
 *
 * ----------------------------------------------------------------------------
 * 【设计原则】
 * ----------------------------------------------------------------------------
 * 当前默认值遵循以下原则：
 *
 * 1. 出生后的默认状态应该是“可运行的中性起点”
 *    - 不能极端
 *    - 不能一出生就崩到很饿、很虚弱、很警觉
 *
 * 2. 默认状态要给后续系统留足波动空间
 *    - 不能全满
 *    - 不能全零
 *    - 要能自然上升/下降
 *
 * 3. 默认状态暂时不直接绑定先天人格
 *    - 当前先做统一默认值
 *    - 后续再通过 personality-core 生成“个性化初始偏移”
 *
 * 4. 所有默认值都应该适合作为出生后第一份状态
 *    - 便于后续由事件、时间、人格逐步拉开差异
 */

import type {
  CognitiveState,
  DriveState,
  EmotionalState,
  PetState,
  PhysicalState,
  RelationalState,
} from "./state-types";

/* -------------------------------------------------------------------------- */
/* 一、情绪状态默认值                                                           */
/* -------------------------------------------------------------------------- */

/**
 * 默认情绪状态
 *
 * 设计思路：
 * - 不极端正向，也不极端负向
 * - 稍微偏平静
 * - 稳定度中等偏上
 *
 * 理由：
 * - 新出生宠物不应该一开始就处在强烈情绪里
 * - 但也不能完全没有可波动空间
 */
export const DEFAULT_EMOTIONAL_STATE: EmotionalState = {
  /**
   * 情绪正负方向
   *
   * 这里给 0，表示默认中性。
   * 后续如果先天人格偏温和、偏敏感，可以在初始化阶段做微调。
   */
  valence: 0,

  /**
   * 情绪激活度
   *
   * 给一个偏低值，表示刚进入世界时整体较平缓，不是高度兴奋或高度紧绷。
   */
  arousal: 0.35,

  /**
   * 情绪稳定度
   *
   * 给中等偏上的值，表示初始状态不至于被轻微事件立刻打爆。
   */
  stability: 0.6,

  /**
   * 当前情绪标签
   *
   * 默认先给 neutral。
   * 后续如果有 classifier，也可以统一由数值重新推导。
   */
  label: "neutral",
};

/* -------------------------------------------------------------------------- */
/* 二、生理状态默认值                                                           */
/* -------------------------------------------------------------------------- */

/**
 * 默认生理状态
 *
 * 设计思路：
 * - 精力较充足，但不是满值
 * - 饥饿较低，但不是零
 * - 身体状态稳定
 * - 恢复倾向中等
 *
 * 理由：
 * - 一出生就能正常活动
 * - 但不至于长时间不进入进食/休息逻辑
 */
export const DEFAULT_PHYSICAL_STATE: PhysicalState = {
  /**
   * 精力
   *
   * 给到较高值，表示刚进入世界时具备行动能力。
   */
  energy: 78,

  /**
   * 饥饿
   *
   * 给较低值，但不为 0。
   * 这样时间推进后可以自然进入 eating 逻辑。
   */
  hunger: 22,

  /**
   * 健康 / 舒适度
   *
   * 默认给较高值，表示身体整体稳定。
   */
  health: 88,

  /**
   * 恢复倾向
   *
   * 给中等值，表示当前没有特别强的恢复需求，但也具备恢复节奏空间。
   */
  recovery: 0.5,

  /**
   * 当前生理标签
   *
   * 默认先给 stable。
   */
  label: "stable",
};

/* -------------------------------------------------------------------------- */
/* 三、认知 / 注意状态默认值                                                    */
/* -------------------------------------------------------------------------- */

/**
 * 默认认知 / 注意状态
 *
 * 设计思路：
 * - 专注度中等
 * - 环境感知中等偏上
 * - 有一点探索兴趣
 * - 压力较低
 *
 * 理由：
 * - 新出生宠物进入世界时，应该具备基础观察能力
 * - 但不应一开始就高度紧张或极度迟钝
 */
export const DEFAULT_COGNITIVE_STATE: CognitiveState = {
  /**
   * 专注度
   *
   * 中等值，表示当前既能稳定持续行为，也保留切换空间。
   */
  focus: 0.5,

  /**
   * 环境感知度
   *
   * 中等偏上，表示会观察周围世界，但不是神经过敏。
   */
  awareness: 0.58,

  /**
   * 探索兴趣
   *
   * 略高于中位，表示进入世界后对周围有一点自然好奇。
   */
  curiosity: 0.55,

  /**
   * 压力 / 认知负担
   *
   * 初始给偏低值，表示默认不处于强烈压力中。
   */
  stress: 0.2,

  /**
   * 当前认知标签
   *
   * 默认先给 observing，比 idle 更贴合刚进入世界的状态。
   */
  label: "observing",
};

/* -------------------------------------------------------------------------- */
/* 四、动力状态默认值                                                           */
/* -------------------------------------------------------------------------- */

/**
 * 默认动力状态
 *
 * 设计思路：
 * - 初始不应该有极端强驱动力
 * - 探索略高一点，符合“进入世界后会有一点走动和观察”的感觉
 * - 休息与进食保留基础值
 * - 接近和回避都先较低
 *
 * 理由：
 * - 后续行为系统需要从这里选出主驱动力
 * - 默认不能全一样，否则 primary 不好定
 */
export const DEFAULT_DRIVE_STATE: DriveState = {
  /**
   * 休息驱动力
   */
  rest: 0.28,

  /**
   * 进食驱动力
   */
  eat: 0.22,

  /**
   * 探索驱动力
   *
   * 默认略高，方便初期更自然地产生 walking / observing 倾向。
   */
  explore: 0.42,

  /**
   * 接近驱动力
   *
   * 当前第一版先保持较低，不主动假设强依附。
   */
  approach: 0.18,

  /**
   * 回避驱动力
   *
   * 初始给低值，表示默认不处于明显退缩状态。
   */
  avoid: 0.12,

  /**
   * 当前主驱动力
   *
   * 由于 explore 数值最高，所以默认给 explore。
   */
  primary: "explore",
};

/* -------------------------------------------------------------------------- */
/* 五、关系状态默认值                                                           */
/* -------------------------------------------------------------------------- */

/**
 * 默认关系状态
 *
 * 设计思路：
 * - 初始安全感和熟悉感中等
 * - 信任感中等偏低到中等
 * - 不直接设成强依附
 * - 也不设成强疏离
 *
 * 理由：
 * - 刚进入世界时，关系不应被写死
 * - 后续要靠事件和轨迹来慢慢拉开
 */
export const DEFAULT_RELATIONAL_STATE: RelationalState = {
  /**
   * 信任感
   *
   * 初始给中等值，不预设高度信任，也不预设不信任。
   */
  trust: 0.5,

  /**
   * 安全感
   *
   * 初始给略高于中位，表示默认世界环境不是立刻造成威胁。
   */
  safety: 0.56,

  /**
   * 依附 / 亲近倾向
   *
   * 初始偏低到中等。
   * 不直接假设“刚出生就已经明显依附”。
   */
  attachment: 0.32,

  /**
   * 疏离 / 防备倾向
   *
   * 初始给低到中等值，表示有基本保留，但不是高度防御。
   */
  distance: 0.26,

  /**
   * 熟悉感
   *
   * 刚进入世界，环境并非完全陌生，但也谈不上非常熟悉。
   */
  familiarity: 0.4,

  /**
   * 当前关系标签
   *
   * 默认先给 neutral。
   */
  label: "neutral",
};

/* -------------------------------------------------------------------------- */
/* 六、宠物总状态默认值                                                         */
/* -------------------------------------------------------------------------- */

/**
 * 默认宠物状态
 *
 * 这是把五大状态默认值打包后的总结构。
 *
 * 注意：
 * 这里使用函数返回，而不是直接导出单个对象常量，
 * 主要是为了避免外部误改共享引用。
 */
export function buildDefaultPetState(): PetState {
  return {
    emotional: { ...DEFAULT_EMOTIONAL_STATE },
    physical: { ...DEFAULT_PHYSICAL_STATE },
    cognitive: { ...DEFAULT_COGNITIVE_STATE },
    drive: { ...DEFAULT_DRIVE_STATE },
    relational: { ...DEFAULT_RELATIONAL_STATE },
  };
}

/* -------------------------------------------------------------------------- */
/* 七、可选辅助方法                                                             */
/* -------------------------------------------------------------------------- */

/**
 * 创建默认情绪状态
 *
 * 作用：
 * - 当你只想单独拿一份默认情绪状态时使用
 * - 避免直接复用常量对象引用
 */
export function buildDefaultEmotionalState(): EmotionalState {
  return { ...DEFAULT_EMOTIONAL_STATE };
}

/**
 * 创建默认生理状态
 */
export function buildDefaultPhysicalState(): PhysicalState {
  return { ...DEFAULT_PHYSICAL_STATE };
}

/**
 * 创建默认认知 / 注意状态
 */
export function buildDefaultCognitiveState(): CognitiveState {
  return { ...DEFAULT_COGNITIVE_STATE };
}

/**
 * 创建默认动力状态
 */
export function buildDefaultDriveState(): DriveState {
  return { ...DEFAULT_DRIVE_STATE };
}

/**
 * 创建默认关系状态
 */
export function buildDefaultRelationalState(): RelationalState {
  return { ...DEFAULT_RELATIONAL_STATE };
}