/**
 * AI-PET-WORLD
 * 时间线系统 - 状态层类型定义（第一版）
 *
 * ----------------------------------------------------------------------------
 * 【这个文件负责什么？】
 * ----------------------------------------------------------------------------
 * 这个文件专门负责定义 timeline-system 中“状态层”的核心类型。
 *
 * 它只定义：
 * - 情绪状态（EmotionalState）
 * - 生理状态（PhysicalState）
 * - 认知/注意状态（CognitiveState）
 * - 动力状态（DriveState）
 * - 关系状态（RelationalState）
 * - 宠物状态总结构（PetState）
 *
 * ----------------------------------------------------------------------------
 * 【这个文件不负责什么？】
 * ----------------------------------------------------------------------------
 * 这个文件不负责：
 * - 默认值初始化
 * - 状态更新逻辑
 * - 状态标签分类逻辑
 * - 生命周期轨迹记录
 * - 时序推演偏移
 * - 玩家关系输入
 *
 * 它只是“状态层的数据结构基础文件”。
 *
 * ----------------------------------------------------------------------------
 * 【状态层在系统中的位置】
 * ----------------------------------------------------------------------------
 * 先天人格固定存在
 * -> 世界事件进入
 * -> 在先天人格框架内形成状态变化
 * -> 状态推动行为
 * -> 行为和事件沉淀为轨迹
 *
 * 所以状态层不是先天人格本身，
 * 而是“先天人格在当前时刻的显化与波动”。
 */

/* -------------------------------------------------------------------------- */
/* 一、基础通用类型                                                             */
/* -------------------------------------------------------------------------- */

/**
 * 比例值
 *
 * 推荐语义：
 * - 一般表示 0 ~ 1 的归一化数值
 * - 用于表达某种倾向、强度、敏感度、稳定度等
 *
 * 这里先只做类型别名，不做运行时范围限制。
 */
export type RatioValue = number

/**
 * 百分比/资源值
 *
 * 推荐语义：
 * - 一般表示 0 ~ 100 的资源型数值
 * - 例如：energy / hunger / health
 */
export type PercentValue = number

/* -------------------------------------------------------------------------- */
/* 二、情绪状态（Emotional State）                                              */
/* -------------------------------------------------------------------------- */

/**
 * 情绪标签
 *
 * 这是给 UI、world 页面、事件文案使用的“结果层标签”。
 *
 * 注意：
 * - 它不是底层唯一真值
 * - 底层变化主要还是由数值维度决定
 * - label 更适合展示和文案
 */
export type EmotionalLabel =
  | "relaxed"   // 放松
  | "content"   // 满足
  | "curious"   // 好奇
  | "excited"   // 兴奋
  | "alert"     // 警觉
  | "irritated" // 烦躁
  | "anxious"   // 不安
  | "low"       // 低落
  | "neutral"   // 中性

/**
 * 情绪状态
 */
export interface EmotionalState {
  /**
   * 情绪正负方向，建议范围 -1 ~ 1
   */
  valence: number

  /**
   * 情绪激活度，建议范围 0 ~ 1
   */
  arousal: RatioValue

  /**
   * 情绪稳定度，建议范围 0 ~ 1
   */
  stability: RatioValue

  /**
   * 当前主情绪标签
   */
  label: EmotionalLabel
}

/* -------------------------------------------------------------------------- */
/* 三、生理状态（Physical State）                                               */
/* -------------------------------------------------------------------------- */

/**
 * 生理标签
 *
 * 给展示层和文案系统使用的抽象结果。
 */
export type PhysicalLabel =
  | "energetic"  // 精力充足
  | "stable"     // 身体状态稳定
  | "hungry"     // 饥饿明显
  | "tired"      // 疲惫
  | "recovering" // 恢复中
  | "weak"       // 虚弱

/**
 * 生理状态
 *
 * 这一类状态描述的是：
 * - 还有没有体力
 * - 当前饿不饿
 * - 身体舒服不舒服
 * - 当前更偏恢复还是偏消耗
 *
 * 这是行为决策最直接的输入来源之一。
 */
export interface PhysicalState {
  /**
   * 精力值，建议范围 0 ~ 100
   */
  energy: PercentValue

  /**
   * 饥饿值，建议范围 0 ~ 100
   */
  hunger: PercentValue

  /**
   * 健康 / 舒适度，建议范围 0 ~ 100
   */
  health: PercentValue

  /**
   * 恢复倾向，建议范围 0 ~ 1
   */
  recovery: RatioValue

  /**
   * 当前生理主标签
   */
  label: PhysicalLabel
}

/* -------------------------------------------------------------------------- */
/* 四、认知/注意状态（Cognitive / Attention State）                             */
/* -------------------------------------------------------------------------- */

/**
 * 认知/注意标签
 *
 * 用于表达当前处理外界刺激的主要方式。
 */
export type CognitiveLabel =
  | "idle"       // 松散 / 空闲
  | "observing"  // 观察中
  | "focused"    // 专注中
  | "curious"    // 对周围有探索兴趣
  | "hesitant"   // 迟疑
  | "stressed"   // 精神压力较高
  | "avoidant"   // 倾向回避

/**
 * 认知 / 注意状态
 */
export interface CognitiveState {
  /**
   * 专注度，建议范围 0 ~ 1
   */
  focus: RatioValue

  /**
   * 环境感知度，建议范围 0 ~ 1
   */
  awareness: RatioValue

  /**
   * 探索兴趣，建议范围 0 ~ 1
   */
  curiosity: RatioValue

  /**
   * 压力 / 认知负担，建议范围 0 ~ 1
   */
  stress: RatioValue

  /**
   * 当前认知主标签
   */
  label: CognitiveLabel
}

/* -------------------------------------------------------------------------- */
/* 五、动力状态（Drive State）                                                  */
/* -------------------------------------------------------------------------- */

/**
 * 主驱动力标签
 *
 * 当前最强的行为方向。
 */
export type PrimaryDrive =
  | "rest"     // 想休息
  | "eat"      // 想进食
  | "explore"  // 想探索
  | "approach" // 想靠近
  | "avoid"    // 想回避
  | "idle"     // 暂无明显主驱动力

/**
 * 动力状态
 */
export interface DriveState {
  /**
   * 休息驱动力，建议范围 0 ~ 1
   */
  rest: RatioValue

  /**
   * 进食驱动力，建议范围 0 ~ 1
   */
  eat: RatioValue

  /**
   * 探索驱动力，建议范围 0 ~ 1
   */
  explore: RatioValue

  /**
   * 接近驱动力，建议范围 0 ~ 1
   */
  approach: RatioValue

  /**
   * 回避驱动力，建议范围 0 ~ 1
   */
  avoid: RatioValue

  /**
   * 当前主驱动力
   */
  primary: PrimaryDrive
}

/* -------------------------------------------------------------------------- */
/* 六、关系状态（Relational State）                                             */
/* -------------------------------------------------------------------------- */

/**
 * 关系标签
 *
 * 给 world 页面、事件文案、状态展示层使用。
 */
export type RelationalLabel =
  | "secure"   // 安全、安心
  | "attached" // 亲近、依附
  | "neutral"  // 中性
  | "guarded"  // 保留、防备
  | "distant"  // 疏离

/**
 * 关系状态
 */
export interface RelationalState {
  /**
   * 信任感，建议范围 0 ~ 1
   */
  trust: RatioValue

  /**
   * 安全感，建议范围 0 ~ 1
   */
  safety: RatioValue

  /**
   * 依附 / 亲近倾向，建议范围 0 ~ 1
   */
  attachment: RatioValue

  /**
   * 疏离 / 防备倾向，建议范围 0 ~ 1
   */
  distance: RatioValue

  /**
   * 熟悉感，建议范围 0 ~ 1
   */
  familiarity: RatioValue

  /**
   * 当前关系主标签
   */
  label: RelationalLabel
}

/* -------------------------------------------------------------------------- */
/* 七、宠物状态总结构（Pet State）                                              */
/* -------------------------------------------------------------------------- */

/**
 * 宠物当前状态总结构
 *
 * 这是五大状态分类的统一入口。
 */
export interface PetState {
  /**
   * 情绪状态
   */
  emotional: EmotionalState

  /**
   * 生理状态
   */
  physical: PhysicalState

  /**
   * 认知 / 注意状态
   */
  cognitive: CognitiveState

  /**
   * 动力状态
   */
  drive: DriveState

  /**
   * 关系状态
   */
  relational: RelationalState
}