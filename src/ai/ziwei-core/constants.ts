/**
 * ======================================================
 * AI-PET-WORLD
 * Personality Core - Constants
 * ======================================================
 *
 * 【文件职责】
 * 这个文件用于存放 personality-core 模块中的公共常量。
 *
 * 当前它不再服务旧版 core_symbol 系统，
 * 而是服务新版：
 *
 *   star + pair + ziwei-engine
 *
 * ------------------------------------------------------
 * 【为什么要单独拆一个 constants.ts】
 * 因为这些值会被多个文件共同使用：
 *
 * - calculator.ts
 * - mapper.ts
 * - knowledge/*
 * - gateway.ts
 * - 测试页面
 *
 * 如果直接把这些值写死在各个文件里，会带来几个问题：
 *
 * 1. 改一个规则时，要到处改
 * 2. 容易出现顺序不一致
 * 3. 维护成本很高
 * 4. 新旧逻辑迁移时很容易漏
 *
 * ------------------------------------------------------
 * 【当前重构后的原则】
 * 这个文件里只保留：
 *
 * 1. 新版业务宫位顺序
 * 2. traits 通用上下限
 * 3. 默认基准值
 * 4. 后续新版权重参数
 *
 * 不再保留：
 * - CoreSymbol
 * - SYMBOL_ORDER
 * - sector_1 ~ sector_12
 *
 * 因为这些属于旧架构，已经和当前 schema.ts 不一致。
 * ======================================================
 */

import type { SectorName } from "./schema"

/**
 * ======================================================
 * 业务宫位顺序
 * ======================================================
 *
 * 说明：
 * 这是当前项目内部统一使用的“业务宫位顺序”。
 *
 * 顺序为：
 * 0  -> life
 * 1  -> siblings
 * 2  -> spouse
 * 3  -> children
 * 4  -> wealth
 * 5  -> health
 * 6  -> travel
 * 7  -> friends
 * 8  -> career
 * 9  -> property
 * 10 -> fortune
 * 11 -> parents
 *
 * ------------------------------------------------------
 * 【用途】
 * - 创建空的 sectors 结构
 * - 前端按固定顺序展示
 * - mapper.ts 遍历业务宫位
 * - 保持项目内部顺序一致
 *
 * ------------------------------------------------------
 * 【注意】
 * 这不是紫微公式坐标顺序，
 * 也不是地支宫位顺序。
 *
 * 这是“业务层表达顺序”。
 * ======================================================
 */
export const SECTOR_ORDER: SectorName[] = [
  "life",
  "siblings",
  "spouse",
  "children",
  "wealth",
  "health",
  "travel",
  "friends",
  "career",
  "property",
  "fortune",
  "parents"
]

/**
 * ======================================================
 * 默认 traits 基准值
 * ======================================================
 *
 * 当前版本中，很多人格维度在没有明显加成/减成前，
 * 会先以这个值作为中位基准。
 *
 * 当前先采用：
 *   50
 *
 * 理解方式：
 * - 0   = 极低
 * - 50  = 中性
 * - 100 = 极高
 *
 * 这是一个比较适合 MVP 阶段的中位数。
 * ======================================================
 */
export const DEFAULT_TRAIT_VALUE = 50

/**
 * ======================================================
 * traits 最小值
 * ======================================================
 *
 * 所有人格数值最终都不应低于这个值。
 * 一般在 mapper.ts 做 clamp 时使用。
 * ======================================================
 */
export const MIN_TRAIT_VALUE = 0

/**
 * ======================================================
 * traits 最大值
 * ======================================================
 *
 * 所有人格数值最终都不应高于这个值。
 * 一般在 mapper.ts 做 clamp 时使用。
 * ======================================================
 */
export const MAX_TRAIT_VALUE = 100

/**
 * ======================================================
 * 命宫主星权重
 * ======================================================
 *
 * 命宫原生主星是当前人格最核心的来源，
 * 所以默认权重最高。
 *
 * 后续 mapper.ts 可以直接引用这个值。
 * ======================================================
 */
export const PRIMARY_STAR_WEIGHT = 1.0

/**
 * ======================================================
 * support 星权重
 * ======================================================
 *
 * supportStars 来自当前项目简化版的“三方四正 / 对宫联动”。
 *
 * 它们应该影响人格，
 * 但不应强于命宫原生主星。
 *
 * 当前先给：
 *   0.4
 *
 * 这个值和你前面讨论的人格向量权重思路是一致的。
 * ======================================================
 */
export const SUPPORT_STAR_WEIGHT = 0.4

/**
 * ======================================================
 * 借星权重
 * ======================================================
 *
 * 当宫位为空时，会借对宫主星。
 *
 * 借星不能和原生主星等权，
 * 当前统一采用：
 *   0.65
 *
 * 这个值也应和 ziwei-engine.ts 里的借星权重保持一致。
 * 如果后面改借星权重，建议同时同步两边。
 * ======================================================
 */
export const BORROWED_STAR_WEIGHT = 0.65

/**
 * ======================================================
 * 空宫虚化系数
 * ======================================================
 *
 * 用于后续 mapper.ts 做“空宫人格虚化”时使用。
 *
 * 含义：
 * - 如果命宫为空，即使借星补入，
 *   最终表达也应该有一定“虚 / 间接 / 不稳定”感
 *
 * 当前先给一个预留值：
 *   0.85
 *
 * 你后面可以根据测试再调。
 * ======================================================
 */
export const EMPTY_PRIMARY_ATTENUATION = 0.85