/**
 * ======================================================
 * 判断某个组合是否启用
 * ======================================================
 *
 * 当前逻辑非常直接：
 * - 在 ENABLED_PAIR_IDS 中 => true
 * - 不在 => false
 *
 * 说明：
 * - mapper.ts 在命中 pairRelation 后，
 *   还会调用 isEnabledPair(pairId)
 * - 所以如果这里漏掉某个 pairId，
 *   即使 pairRelations.ts 和 pairProfiles/* 都写对了，
 *   组合依然不会生效
 * ======================================================
 */
export function isEnabledPair(pairId: string): boolean {
  return ENABLED_PAIR_IDS.has(pairId)
}

/**
 * ======================================================
 * 获取全部启用组合 ID（调试 / 测试用）
 * ======================================================
 */
export function getEnabledPairIds(): string[] {
  return Array.from(ENABLED_PAIR_IDS)
}